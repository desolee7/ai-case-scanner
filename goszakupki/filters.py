def calculate_relevance(text: str) -> tuple:
    """
    Возвращает (score, matched_keywords, is_excluded).
    is_excluded = True если сработал негативный паттерн.
    """
    text_lower = text.lower()
    score = 0
    matched = []
    
    # Негативные паттерны — если есть, исключаем сразу
    for pattern, weight in NEGATIVE_PATTERNS.items():
        if pattern.lower() in text_lower:
            return 0, [], True
    
    # Позитивные паттерны
    for pattern, weight in POSITIVE_PATTERNS.items():
        if pattern.lower() in text_lower:
            score += weight
            matched.append(pattern)
    
    return score, matched, False